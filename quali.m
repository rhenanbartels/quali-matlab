function openImage(hObject, ~)
% Import Images
handles = guidata(hObject);
if isfield(handles.data, 'lastVisitedFolder')
    rootPath = uigetdir(handles.data.lastVisitedFolder,...
        'Select a folder with Dicom images');
else
