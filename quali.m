 function [Rmin, Rmax, Win, LevV] = windowCoeffAdj(Img)
    Win = double(max(Img(:)) - min(Img(:)));
    %Win (Win < 1) = 1;
    LevV = double(min(Img(:)) + (Win/2));
    [Rmin, Rmax] = WL2R(Win, LevV);
 end
 
 function [Rmn, Rmx] = WL2R(W,L)
    Rmn = L - (W/2);
    Rmx = L + (W/2);
    if (Rmn >= Rmx)
        Rmx = Rmn + 1;
    end
 end

